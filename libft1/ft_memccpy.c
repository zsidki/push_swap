#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	*s1;
	unsigned char	*s2;

	s1 = (unsigned char *)dst;
	s2 = (unsigned char *)src;
	i = 0;
	while (n > i)
	{
		s1[i] = s2[i];
		if ((unsigned char)c == s2[i])
			return (dst + i + 1);
		i++;
	}
	return (NULL);
}
